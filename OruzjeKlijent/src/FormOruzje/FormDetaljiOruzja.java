    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                + "obrisete ovo oruzje?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {
                ClientController.getInstance().deleteOruzje(o);
                FormPretragaOruzja fp = (FormPretragaOruzja) getParent();
                fp.refreshTable();
                fp.popuniOruzja();
                JOptionPane.showMessageDialog(this, "Uspesno obrisano oruzje.");
                this.dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Ne mozete da obrisete ovo oruzje "
                        + "jer je bilo naruceno!");
//                Logger.getLogger(FormDetaljiOruzja.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

        try {

            if (txtNaziv.getText().isEmpty() || txtOpis.getText().isEmpty()
                    || txtCena.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
                return;
            }

            String naziv = txtNaziv.getText();
            String opis = txtOpis.getText();
            double cena = Double.parseDouble(txtCena.getText());

            o.setNazivOruzja(naziv);
            o.setCena(cena);
            o.setOpis(opis);
            
            ClientController.getInstance().updateOruzje(o);
            FormPretragaOruzja fp = (FormPretragaOruzja) getParent();
            fp.refreshTable();
            fp.popuniOruzja();
            JOptionPane.showMessageDialog(this, "Uspesno izmenjeno oruzje.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
//            Logger.getLogger(FormDetaljiOruzja.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnIzmeniActionPerformed


